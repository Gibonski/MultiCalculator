package com.example.multicalculator

class Calculator{
    fun Add(
        left: Int,
        right: Int
    ): Int {
        return left + right
    }
    fun Subtract(
        left: Int,
        right: Int
    ): Int {
        return left - right
    }
    fun Division(
        left: Int,
        right: Int
    ): Int {
        return left / right
    }
    fun Multiplication(
        left: Int,
        right: Int
    ): Int {
        return left * right
    }

}