package com.example.multicalculator

class Calculator{
    fun add(
        left: Int,
        right: Int
    ): Int {
        return left + right
    }
    fun subtract(
        left: Int,
        right: Int
    ): Int {
        return left - right
    }
    fun division(
        left: Int,
        right: Int
    ): Int {
        return left / right
    }
    fun multiplication(
        left: Int,
        right: Int
    ): Int {
        return left * right
    }

}